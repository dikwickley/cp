bool reachDestination(int sx, int sy, int dx, int dy) {


    if(sx==dx && sy==dy){
        return true;
    }

    bool t = false;

    if(sx+sy <= dx){
       t = t || reachDestination(sx+sy, sy, dx, dy);
    } 

    if(sx+sy <= dy){
       t = t || reachDestination(sx, sx+sy, dx, dy);
    } 

    return t;

}

