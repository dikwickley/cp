vector<int> sieve(int n) {
	vector<bool> p(n + 1, true);
	p[1] = false;
	for (int i = 2; i * i <= n; i++)
		if (p[i] == true)
			for (int j = i * i; j <= n; j += i)
				p[j] = false;
	vector<int> primes;
	for (int i = 2; i <= n; i++)
		if (p[i])
			primes.push_back(i);
	return primes;
}

