int solve(int n, vector<int> &a) {
	vector < pair<int, int>> w(n + 1); //right, left
	for (int i = 0; i < n + 1; i++) {
		if (a[i] != 0) {
			w[i].second = i - a[i];
			w[i].first = i + a[i];
		}

	}
	sort(w.begin(), w.end(), greater<pair<int, int>>());
	vector<pair<int, int>> temp = {w[0]};
	for (int i = 0, j = 0; i < n + 1; i++) {
		if (w[i].first == temp[j].first) {
			if (w[i].first - w[i].second > temp[j].first - temp[j].second) {
				temp[j].first = w[i].first;
				temp[j].second = w[i].second;
			}
		} else {
			j++;
			temp.push_back(w[i]);
		}
	}
	w = temp;

	debug(w);

	for (int i = 0; i < n + 1; i++) {
		if (w[i].first >= n && w[i].second <= 0)
			return 1;
	}
	int count = 1;
	int last_left = w[0].second;

	if (w[0].first < n) {
		return -1;
	}

	for (int i = 1; i < n + 1; i++) {



		if (last_left <= 0) {
			// cout << count << endl; return;
			return count;
		}

		// if (w[i].second > last_left) {
		// 	// cout << -1 << endl; return;
		// 	return -1;
		// }

		if (w[i].first >= last_left ) {
			last_left = w[i].second;
			count++;
		} else {
			return -1;
		}
	}


	if (last_left <= 0) {
		return count;
	}
	else {
		return -1;
	}


}

