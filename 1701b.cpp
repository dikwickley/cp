    for(int i=1;i<=n;i++){
    	if(m[i]==0){

    		int current = i;
    		while(current <= n){
    			m[current] = 1;
    			p.push_back(current);
    			current = current*d;

    		}

    	}
    }
    

    for(int i=1;i<=n;i++){
    	if(m[i]==0){
    		p.push_back(i);
    	}
    }
    cout<<d<<endl;
