int minSwaps(vector<int> &arr)
{
    int n = arr.size();

    vector<pair<int,int>> a(n);

    for(int i=0;i<n;i++){
        a[i] = {arr[i],i};
    }

    sort(a.begin(), a.end());

    int count = 0;

    for(int i=0;i<n;i++){
        if(a[i].second != i){
            count++;
            swap(a[i], a[a[i].second]);
            i--;
        }
    }


    return count;
}

