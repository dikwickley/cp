void solve() {
	int a, b, ab, ba;
	cin >> a >> b >> ab >> ba;

	string s;
	cin >> s;



	int n = s.size();

	int a_count = 0;
	int b_count = 0;
	for (int i = 0; i < n; i++) {
		if (s[i] == 'A') a_count += 1;
		if (s[i] == 'B') b_count += 1;
	}

	if (a_count != a + ab + ba) {
		cout << "NO" << endl; return;
	}

	if (b_count != b + ab + ba) {
		cout << "NO" << endl; return;
	}


	vector<int> gp_ab; //ab
	vector<int> gp_ba; //ba
	vector<int> gp_common; // aba or bab

	string window = "";
	debug(a, b, ab, ba);
	debug(s);

	for (int i = 0; i < s.size(); i++) {
		if (s[i - 1] == s[i]) {
			if (window[0] == 'A' && window.size() > 1) {
				if (window[window.size() - 1] == 'B') gp_ab.push_back(window.size());
				if (window[window.size() - 1] == 'A') gp_common.push_back(window.size());
			}
			if (window[0] == 'B' && window.size() > 1) {
				if (window[window.size() - 1] == 'A') gp_ba.push_back(window.size());
				if (window[window.size() - 1] == 'B') gp_common.push_back(window.size());
			}
			window = "";
		}
		window +=  s[i];
	}

	if (window[0] == 'A' && window.size() > 1) {
		if (window[window.size() - 1] == 'B') gp_ab.push_back(window.size());
		if (window[window.size() - 1] == 'A') gp_common.push_back(window.size());
	}
	if (window[0] == 'B' && window.size() > 1) {
		if (window[window.size() - 1] == 'A') gp_ba.push_back(window.size());
		if (window[window.size() - 1] == 'B') gp_common.push_back(window.size());
	}



	sort(gp_ab.begin(), gp_ab.end());
	sort(gp_ba.begin(), gp_ba.end());
	sort(gp_common.begin(), gp_common.end());

	debug(gp_ab);
	debug(gp_ba);
	debug(gp_common);

	debug(ab, ba);

	for (auto i : gp_ab) {
		if (ab > 0) {
			ab -= i / 2;
			if (ab < 0) {
				ba -= -1 * (ab + 1);
				a = 0;
			}
		} else {
			ba -= (i - 2) / 2;
		}
	}

	for (auto i : gp_common) {
		if (ab > 0) {
			ab -= i / 2;
			if (ab < 0) {
				ba -= -1 * ab;
				a = 0;
			}
		} else {
			ba -= i / 2;
		}
	}

	for (auto i : gp_ba) {
		if (ab > 0) {
			ab -= (i - 2) / 2;
			if (ab < 0) {
				ba -= -1 * ab;
				a = 0;
			}
		} else {
			ba -= i / 2;
		}
	}

	if (ab > 0 || ba > 0) {
		cout << "NO" << endl; return;
	}

	debug(ab, ba);

	cout << "YES" << endl; return;



}

