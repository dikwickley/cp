const int mod = 1e9 + 7;
int dp[1000001] = {0};
int topdown(int n, int k) {
    if (n == 0) {
        return 1;
    }
    if (n < 0) {
        return 0;
    }

    if (dp[n] != 0) return dp[n];

    int sum = 0;
    for (int i = 1; i <= k; i++) {
        dp[n - i] = topdown(n - i, k);
        sum += dp[n - i];
    }

    return sum;
}

int dp2[1000001] = {0};

int bottomup(int n, int k) {
    for (int i = 0; i < k - 1; i++) {
        dp2[i] = 1;
    }

    for (int i = k - 1; i <= n; i++) {
        for (int j = 1; j <= k; j++) {
            dp2[i] += dp2[i - j];
        }
    }

    return dp2[n];
}

